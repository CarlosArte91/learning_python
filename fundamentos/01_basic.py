# Hello world
print("Hello world!\n\n")


""" Operaciones básicas en programación con Python. """
print("Operaciones básicas\n")

# Suma
print("La suma es =>", 1 + 3)

# Resta
print("La resta es =>", 55 - 28)

# Multiplicación
print("La multiplicación es =>", 6 * 9)

# División
print("La división es =>", 78 / 6)

# Potencia o exponencial
print("La potencia es =>", 5 ** 3)

#  Modulo o restante
print("El módulo es =>", 3 % 2)

# División exacta (El resultado es un entero)
print("La división exacta es =>", 25 // 6, "\n\n")


""" Typos de datos en Python """
print("Typos de datos\n")

print("10 =>", type(10))
print("3.1416 =>", type(3.1416))
print("1 + 3j =>", type(1 + 3j))
print("Hello python =>", type("Hello python"))
print("True =>", type(True))
print("False =>", type(False))
print("[1, 2, 3] =>", type([1, 2, 3]))
print("{'first_name': 'Carlos'} =>", type({'first_name': 'Carlos'}))
print("{9.8, 3.14, 6.24} =>", type({9.8, 3.14, 6.24}))
print("(9.8, 3.14, 6.24) =>", type((9.8, 3.14, 6.24)))
